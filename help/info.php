                            <div class="inner-style inner-pos">
                                <span>device&nbsp;care&nbsp;tips</span>
                            </div>
                            <div class="inner-style inner-pos">
                                <div>
                                    <span>
                                        Handle your device, battery, charger and accessories with care.&nbsp;The following suggestions help you protect your device&colon;
                                    </span><br><br>
                                    <div>
                                        <ul type="none">
                                            <li>
                                                <ul type="square">
                                                    <li>
                                                        Keep the device dry. Precipitation, humidity, and all types of liquids or moisture can contain minerals that corrode electronic circuits.<br>If your device
                                                        gets wet, remove the battery, and allow the device to dry.<br><br> 
                                                    </li>
                                                    <li>
                                                        Do not use or store the device in dusty or dirty areas. Moving parts and electronic circuits components can be damaged.<br><br>
                                                    </li>
                                                    <li>
                                                        Do not store the device in high temperatures. High temperatures can shorten the life of the device, damage the battery, and warp or melt plastics.<br><br>
                                                    </li>
                                                    <li>
                                                        Do not store the device in cold temperatures, When the device warms to its normal temperature, moisture can form inside the device<br>and damage electronic circuits.<br><br> 
                                                    </li>
                                                    <li>
                                                        Unauthorised modifications may damage the device and violate regulations governing audio devices.<br><br>
                                                    </li>
                                                    <li>
                                                        Do not drop, knock, or shake the device. Rough handling can break internal circuit boards and mechanics.<br><br> 
                                                    </li>
                                                    <li>
                                                        Only use a soft, clean, dry cloth to clean the surface of the device.<br><br>
                                                    </li>
                                                    <li>
                                                        Do not paint the device. Paint can clog the moving parts and prevent proper operation.<br><br>
                                                    </li>
                                                    <li>
                                                        Switch off the device and remove the battery from time to time for optimum performance.<br><br>
                                                    </li>
                                                    <li>
                                                        Keep the device away from magnets or magnetic fields.<br><br>
                                                    </li>
                                                    <li>
                                                        To keep your important data safe, store it in at least two separate places, such as the device, memory card, or computer, or<br>write down important information.<br><br>
                                                    </li>
                                                </ul>
                                            </li>
                                        </ul>
