                                        <span>RF&nbsp;Exposure</span><br>
                                        <span>
                                            Your phone contains a transmitter and receiver. When it is <strong>ON</strong>, it receives and transmits <b title="Radio Frequency">RF</b>
                                            energy. When you communicate with your phone, the system handling your call controls the power level at which your phone transmits.
                                        </span>
                                        <span><br>
                                            Body&nbsp;worn&nbsp;operation
                                        </span><br>
                                            <span>Important&nbsp;safety information regarding radiofrequency radiation (RF) exposure&colon;</span>
                                            <div>
                                            <ul type="none">
                                            </li>
                                                <ul type="square">
                                            <li>To ensure compliance with RF exposure guidelines the phone must be used with a minimum of 1.5 centimetres (5/8 inch)<br>away from the body.
                                                &nbsp;Any carry case, belt chip, or holder for body-worn operation should not contain metal and should position the<br>device 1.5 cm distance from your body.
                                            </li>
                                            <li>
                                                Failure to observe these instructions could result in your RF exposure exceeding the relevant guideline limits.
                                            </li>    
                                                </ul>
                                            </li>  
                                            </ul>
                                         </div><br>   
                                        <span>
                                            Limiting exposure to radio frequency (RF) fields
                                        </span><br>
                                        <span>
                                            For individuals concerned about limiting their exposure to RF fields, the World Health Organisation (WHO) provides the following advice&colon;
                                            </span><br>
                                            <div>
                                            <ul style="list-style-type: none;">
                                            <li>Precautionary measures:<br><!--<br>-->
                                                <ul style="list-style-type: none;">
                                                    <li>Present scientific information does not indicate the need for any special precautions for the use of mobile phones.</li>
                                                    <li>If individuals are concerned, they might choose to limit their own or their children's RF by limiting the length of calls, or using</li>
                                                    <li>'hands-free' devices such as <b>Headsets</b> to keep mobile phones away from the head and body.<br><br></li>
                                                    <li>Further information on this subject can be obtained from the <strong title="World Health Organisation">WHO</strong> home page: <a href="http://www.who.int/" target="_blank">http://www.who.int/</a></li>
                                                </ul>
                                            </li>                                            
                                            </ul>
                                        </div><br>
